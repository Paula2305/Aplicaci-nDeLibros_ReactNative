export class Libro {
    constructor(id, titulo, autor, ISBN, precio, genero, sinopsis, url_foto) {
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.ISBN = ISBN;
        this.precio = precio;
        this.genero = genero;
        this.sinopsis = sinopsis;
        this.url_foto = url_foto;
    }
}